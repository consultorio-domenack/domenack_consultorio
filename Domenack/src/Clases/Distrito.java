package Clases;

public class Distrito {
    private String id;
    private String distrito;

    public Distrito(String id, String distrito) {
        this.id = id;
        this.distrito = distrito;
    }

    public Distrito() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getDistrito() {
        return distrito;
    }

    public void setDistrito(String distrito) {
        this.distrito = distrito;
    }
    
    
}
